# Problem Statement: Write a Python program that takes two numbers as input from the user and prints their sum.

# Source Code (python) ------------------------

# Take the first number as input and convert it to an integer
num1 = int(input("Enter the first number: "))

# Take the second number as input and convert it to an integer
num2 = int(input("Enter the second number: "))

# Calculate the sum of the two numbers
total = num1 + num2

# Print the sum
print("The sum is:", total)
